package Array;

public class Main{
	public static void main(String[] args){

		// Declare an array
		int[] arr = new int[5];

		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
		System.out.println(arr[4]);

		// Intially elements of the array are 0
		// How we can change the values of array

		arr[0] = 5;
		arr[1] = 23;
		arr[3] = 50;
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
		System.out.println(arr[4]);


		
	}
}
